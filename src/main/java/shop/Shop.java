package shop;

import accessories.DrumStick;
import items.Item;

import java.util.ArrayList;

public class Shop {

    Shop shop;
    Item item;
    DrumStick drumStick;
    ArrayList<Item> stock;
    private String name;

    public Shop(Item item, DrumStick drumStick, String name) {
        this.shop = shop;
        this.item = item;
        this.drumStick = drumStick;
        this.stock = stock;
        this.name = name;
    }



}
